package com.oneonline.backend.model.enums;

/**
 * Enum representing the possible colors of cards in the ONE card game.
 *
 * Colors:
 * - RED: Red colored cards
 * - YELLOW: Yellow colored cards
 * - GREEN: Green colored cards
 * - BLUE: Blue colored cards
 * - WILD: Special color for wild cards (no specific color)
 */
public enum CardColor {
    RED,
    YELLOW,
    GREEN,
    BLUE,
    WILD
}
